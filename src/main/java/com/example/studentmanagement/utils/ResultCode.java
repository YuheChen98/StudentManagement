package com.example.studentmanagement.utils;


/**
 * Constants defining standard response codes for the Student Management System.
 * These response codes are used to indicate the success or failure of API operations.
 *
 * @author Yuhe Chen
 * date: May 9th 2024
 */
public interface ResultCode {
    /**
     * Response code for successful operations.
     */
    Integer SUCCESS = 20000;

    /**
     * Response code for erroneous operations.
     */
    Integer ERROR = 20001;
}
